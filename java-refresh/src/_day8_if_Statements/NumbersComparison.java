package _day8_if_Statements;

public class NumbersComparison {
	public static void main(String[]args) {
		int num1, num2;
		
		num1 = 100;
		num2 = 100; 
		
		if (num1==num2) {
			System.out.println("Numbers are equal");
		}else if(num1>num2) {
			System.out.println("num1 is greater");
		}else if(num1<num2) {
			System.out.println("num2 is greater");
		}
		
		
		
	}
}
