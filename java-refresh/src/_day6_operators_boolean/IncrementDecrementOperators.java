package _day6_operators_boolean;

public class IncrementDecrementOperators {
	public static void main (String[]args) {
		
		int windowsComputer = 35;
		//increase by one 
		windowsComputer++;
		windowsComputer++;
		System.out.println(windowsComputer);
		
		//decrement by one 
		windowsComputer--;
		windowsComputer--;
		System.out.println(windowsComputer);

	}
}
